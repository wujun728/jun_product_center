<template>
	<view class="content">
		<view class="navbar">
			<view 
				v-for="(item, index) in navList" :key="index" 
				class="nav-item" 
				:class="{current: tabCurrentIndex === index}"
				@click="tabClick(index)"
			>
				{{item.text}}
			</view>
		</view>

		<swiper :current="tabCurrentIndex" class="swiper-box" duration="300" @change="changeTab">
			<swiper-item class="tab-content" v-for="(tabItem,tabIndex) in navList" :key="tabIndex">
				<scroll-view 
					class="list-scroll-content" 
					scroll-y
					@scrolltolower="loadData"
				>
					<!-- 空白页 -->
					<empty v-if="tabItem.loaded === true && tabItem.orderList.length === 0"></empty>
					
					<!-- 订单列表 -->
					<view 
						v-for="(item,index) in tabItem.orderList" :key="index"
						class="order-item"
					>
						<view class="i-top b-b">
							<text class="time">订单编号:{{item.orderNo}}</text>
							<text class="state" :style="{color: orderStatusExp(item.status).statusTipColor}">{{orderStatusExp(item.status).statusTip}}</text>
							<!-- <text 
								v-if="item.status==='-1'" 
								class="del-btn yticon icon-iconfontshanchu1"
								@click="deleteOrder(index)"
							></text> -->
						</view>
						
						<scroll-view v-if="item.productJSON.goodsData.length > 1" class="goods-box" scroll-x>
							<view
								v-for="(goodsItem, goodsIndex) in item.productJSON.goodsData" :key="goodsIndex"
								class="goods-item"
							>
								<image class="goods-img" :src="imgPath(goodsItem.img)" mode="aspectFill"></image>
							</view>
						</scroll-view>
						<view 
							v-if="item.productJSON.goodsData.length === 1" 
							class="goods-box-single"
							v-for="(goodsItem, goodsIndex) in item.productJSON.goodsData" :key="goodsIndex"
						>
							<image class="goods-img" :src="imgPath(goodsItem.img)" mode="aspectFill"></image>
							<view class="right">
								<text class="title clamp">{{goodsItem.name}}</text>
								<text class="attr-box">{{goodsItem.remark}}  x {{goodsItem.num}}</text>
								<text class="price">{{goodsItem.price}}</text>
							</view>
						</view>
						
						<view class="price-box">
							共
							<text class="num">{{item.num}}</text>
							件商品 实付款
							<text class="price">{{item.price}}</text>
						</view>
						<view class="action-box b-t" v-if="item.status != '-1'">
							<button class="action-btn" @click="cancelOrder(item)">取消订单</button>
							<button class="action-btn recom" @click="payOrder(item)">立即支付</button>

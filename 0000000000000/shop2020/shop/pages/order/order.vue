<script>
	import uniLoadMore from '@/components/uni-load-more/uni-load-more.vue';
	import empty from "@/components/empty";
 	export default {
		components: {
			uniLoadMore,
			empty
		},
		data() {
			return {
				tabCurrentIndex: 0,
				navList: [{
						state: 0,
						text: '全部',
						loadingType: 'more',
						orderList: []
					},
					{
						state: 1,
						text: '待付款',
						loadingType: 'more',
						orderList: []
					},
					{
						state: 2,
						text: '待收货',
						loadingType: 'more',
						orderList: []
					},
					{
						state: 3,
						text: '待评价',
						loadingType: 'more',
						orderList: []
					},
					{
						state: 4,
						text: '售后',
						loadingType: 'more',
						orderList: []
					}
				],
				listQuery: { //查询分页
					name:"",
					state: 0,
					pageNo: 1,
					pageSize: 10
				},
			};
		},
		
		onLoad(options){
			/**
			 * 修复app端点击除全部订单外的按钮进入时不加载数据的问题
			 * 替换onLoad下代码即可
			 */
 			this.tabCurrentIndex = +options.state;
			// #ifndef MP
			this.loadData()
			// #endif
			// #ifdef MP
			if(options.state == 0){
				this.loadData()
			}
			// #endif
			
		},
		 
		methods: {
			imgPath:function(path){
				if(path==''||path==null||path=='null'){
					return "/static/errorImage.jpg"				 
				}else if (path!=''&&path.indexOf('http')>=0) {
					return path;				 
				}
				return this.siteHost + path;
			},
			//获取订单列表
			loadData(source){
				//这里是将订单挂载到tab列表下
				let index = this.tabCurrentIndex;
				this.listQuery.state=index;//接口赋值
				let navItem = this.navList[index];
 				
				if(source === 'tabChange' && navItem.loaded === true){
					//tab切换只有第一次需要加载数据
					return;
				}
				if(navItem.loadingType === 'loading'){
					//防止重复加载
					return;
				}
				
				navItem.loadingType = 'loading';
				
				setTimeout(()=>{
					let goodsPage={};
					let _this=this;
					_this.sendRequest({
						url : "get_my_order",
						data : _this.listQuery,
						method : "get",
 						success:function (res) {
							goodsPage= res.data;  
							let orderList = goodsPage.datas;
							if(_this.listQuery.pageNo === 1){
								navItem.orderList = [];
							}
							navItem.orderList = navItem.orderList.concat(orderList);  
							//判断是否还有下一页，有是more  没有是nomore(测试数据判断大于20就没有了)
							navItem.loadingType = goodsPage.pageNo<=goodsPage.totalPage ? 'nomore' : 'more';
							// if(type === 'refresh'){
							// 	if(loading == 1){
							// 		uni.hideLoading()
							// 	}else{
							// 		uni.stopPullDownRefresh();
							// 	}
							// }
						}
					});
					 
				}, 600);	
			}, 

			//swiper 切换
			changeTab(e){
				this.tabCurrentIndex = e.target.current;
				this.loadData('tabChange');
			},
			//顶部tab点击
			tabClick(index){
				this.tabCurrentIndex = index;
			},
			//删除订单
			deleteOrder(index){
				uni.showLoading({
					title: '请稍后'
				})
				setTimeout(()=>{
					this.navList[this.tabCurrentIndex].orderList.splice(index, 1);
					uni.hideLoading();
				}, 600)
			},
			//取消订单
			cancelOrder(item){
				uni.showLoading({
					title: '请稍后'
				})
				setTimeout(()=>{ 
					var _this = this;
					//获取数据
					this.sendRequest({
						url : "cancel_order",
						data : {id : item.id},
						method : "get",
 						success:function (res) {
							if(res.code=='200'){
								_this.$api.msg(res.message);
								_this.loadData();
								uni.hideLoading();
							}else{
								_this.$api.msg(res.message);
							}
						}
					}); 
					
				}, 600)
			},
			payOrder(item){
				uni.navigateTo({
					url: `/pages/money/pay?orderNo=${item.orderNo}`
				})
			},
			//订单状态文字和颜色
			orderStatusExp(status){
				let statusTip = '',
					statusTipColor = '#fa436a';
				switch(status){
					case "0":
						statusTip = '交易中'; break;
					case "1":
						statusTip = '交易完成'; break;
					case "-1":
						statusTip = '订单已关闭'; 
						statusTipColor = '#909399';
						break;
						
					//更多自定义
				}
				return {statusTip, statusTipColor};
			}
		},
	}
</script>


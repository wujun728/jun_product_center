<script>
	import share from '@/components/share';
	import uniNumberBox from '@/components/uni-number-box';
	export default{
		components: {
			share,uniNumberBox
		},
		data() {
			return {
				buyClass: 'none',//购买的时候选择规格和数量
				specClass: 'none',
				specSelected:[], 				
				specSelectedMap:{}, 				
 				product:{//产品详情
					imgList:[],
					content:""
				},
				specInfo:{//规格参数
					
				},
				shareList: [], 
				num:1,
			};
		},
		async onLoad(options){ 
			//接收传值,id里面放的是标题，因为测试数据并没写id 
			let id = options.id;
			if(id){//加载数据
 				let _this=this;
				this.sendRequest({
					url : "get_product",
					data : {id:id},
					method : "get",
 					success:function (res) {
						if(res.data){
							_this.product=res.data;
							if(_this.product.attributesList){ 
								_this.product.attributesList.forEach(item=>{
  									for(let it of item.attrs){
 										// _this.specSelectedMap[item.name]="";
 										_this.specSelectedMap[item.name]=it;
										_this.specSelected.push(it);
										break; //forEach不能使用break
									}
								});
								//给多规格的默认选中值
								_this.product.productAttrList.forEach(item=>{
									if(item.attributes==JSON.stringify(_this.specSelected)){
										_this.specInfo=item;
									}
								})
								//调用接口访问量  
								_this.countView();
							}
 						}
					}
				}); 
			}  
		},
		methods:{
			imgPath:function(path){
				if(path==''||path==null||path=='null'){
					return "/static/errorImage.jpg"				 
				}else if (path!=''&&path.indexOf('http')>=0) {
					return path;				 
				}
				return this.siteHost + path;
			},
			//统计访问量
			countView(){
				this.sendRequest({
					url : "count_view",
					data : {id : this.product.count.id},
					method : "get",
 					success:function (res) { 
					}
				}); 
			},
			//规格弹窗开关
			toggleSpec() {
				if(this.specClass === 'show'){
					this.specClass = 'hide';
					setTimeout(() => {
						this.specClass = 'none';
					}, 250);
				}else if(this.specClass === 'none'){
					this.specClass = 'show';
				}
			},
			//选择规格
			selectSpec(index, spec,item){ 
				this.$set(this.specSelectedMap, item.name, spec); 
				//存储已选择
				/**
				 * 修复选择规格存储错误
				 * 将这几行代码替换即可
				 * 选择的规格存放在specSelected中
				 */ 
 				this.specSelected = []; 
				for(var key in this.specSelectedMap){
 					this.specSelected.push(this.specSelectedMap[key]); 
				} 
				this.product.productAttrList.forEach(item=>{
					if(item.attributes==JSON.stringify(this.specSelected)){
						this.specInfo=item;
					}
				}) 
 			},
			//分享
			share(){
				this.$refs.share.toggleMask();	
			},
			//收藏
			toCollect(){
				let _this=this;
				_this.sendRequest({
					url : "collect_save",
					data : {referid : _this.product.id,refer : "Product"},
					method : "get",
					success:function (res) { 
						if(res.code=='200'){
							_this.product.collected = !_this.product.collected;
						}else{
							_this.$api.msg(res.message);  
						}
					}
				});  
			},
			//加入购物车
			cart(){
				this.buyClass="none";
				let attr_id="";
				if(this.product.productAttrList&&this.product.productAttrList.length>0){
					if(!this.specInfo||!this.specInfo.id){
						this.toggleSpec();//显示选择规格
						return;
 					}
				}
				if(this.specInfo&&this.specInfo.id>0){
					attr_id=this.specInfo.id;
				} 
				//只有开启的时候才调用接口
				if(this.specClass === 'show'){
					addCart(this.product.id,this.num,attr_id,JSON.stringify(this.specSelected),this);
				} 
				this.toggleSpec();
				//重新设置成1
				this.num=1;
 				// uni.navigateTo({
				// 	url: `/pages/order/createOrder`
				// })
			},
			buy(){
				this.buyClass="buy";
				let attr_id="";
				if(this.product.productAttrList&&this.product.productAttrList.length>0){
					if(!this.specInfo||!this.specInfo.id){
						this.toggleSpec();//显示选择规格
						return;
					}
				}
				if(this.specInfo&&this.specInfo.id>0){
					attr_id=this.specInfo.id;
				}  
 				//只有开启的时候才调用接口
				if(this.specClass === 'show'){
					addOrder(this.product.id,this.num,attr_id,JSON.stringify(this.specSelected),this);
				} 
				this.toggleSpec();
				//重新设置成1
				this.num=1; 
			},
			//数量改变
			numberChange(e){
 				this.num=e.number;
			},
			stopPrevent(){}
		},

	}
	//添加购物车
	function addCart(referid,num,attr_id,remark,obj){
		let _this=obj;
		let data={
			referid:referid,
			num:num,
			attr_id:attr_id,
			remark:remark,
		}
		_this.sendRequest({
			url : 'save_cart',
			data : data,
			method : "post",
			success:function (res) {
				if(res.code=='200'){
  					_this.$api.msg(`加入购物车成功`);  
				}else{
					_this.$api.msg(res.message);  
				}
			}
		}); 
	}
	//进入下单
	function addOrder(referid,num,attr_id,remark,obj){
		let _this=obj;
		let goodsData = [];
		goodsData.push({
			referid:referid,
			refer:"Product",
			num:num,
			attr_id:attr_id,
			remark:remark,
		});
 		_this.sendRequest({
			url : 'create_order',
			data : {goodsData:goodsData},
			method : "post",
			success:function (res) {
				if(res.code=='200'){
					uni.navigateTo({
						url: `/pages/order/createOrder?orderNo=${res.data.orderNo}`
					})
				}else{
					_this.$api.msg(res.message);
				}
			}
		}); 
	}
</script>

